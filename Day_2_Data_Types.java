public class Day_2_Data_Types{

    public static void main(String [] args){

        byte age = 34;
        short age2 = 87;
        int age3 = 56;
        long age4 = 3455555555555L;
        float f1 = 5.56f;
        double d1 = 5.566d;
        boolean b1 = true;
        char c1 = 'T'; 
        System.out.println("byte age:" + age);
        System.out.println("short age2:" + age2);
        System.out.println("int age3:" + age3);
        System.out.println("long age4:" + age4);
        System.out.println("float f1:" + f1);
        System.out.println("double d1:" + d1);
        System.out.println("boolean b1:" + b1);
        System.out.println("char c1:" + c1);
    }
}