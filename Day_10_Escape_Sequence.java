public class Day_10_Escape_Sequence {
    public static void main(String[] args) {
        
        System.out.println("My name is \" Tushar (Double quote)"); //My name is " Tushar (Double quote)

        System.out.println("My name is \' Tushar (Single quote)"); //My name is ' Tushar (Single quote)

        System.out.println("My name is \\ Tushar (Backslash)"); //My name is \ Tushar (Backslash)

        System.out.println("My name is \t Tushar (tab)"); //My name is       Tushar (tab)

        System.out.println("My name is\b Tushar (Backspace)"); //My name i Tushar (Backspace)

        System.out.println("My name \r is  Tushar(Carriage return)"); // is  Tushar(Carriage return)
         
        System.out.println("My name \f is Tushar(Formfeed)"); //My name 
                                                                //is Tushar(Formfeed)

        System.out.println("My name \n is Tushar(Newline)"); //My name 
                                                               //is Tushar(Newline)
    }
    
}
